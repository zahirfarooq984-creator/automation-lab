print('Hello from Ch3-Lists!')
# Create array/list called cars and add some entries
cars = ['Toyota','Honda','Ford','Audi']
# Print the first index entry Toyota
print(cars[0].title())
# Print the third index entry
print(cars[2].title())

# Return last item in list
print(cars[-1].title())

# Create a message and add a value from the list
message = f"My first car was a {cars[0].title()}."
print(message)
